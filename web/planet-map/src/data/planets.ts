function generateSpectrum(
  dips: { pos: number; depth: number; width: number }[]
): { wavelength: number; flux: number }[] {
  const points: { wavelength: number; flux: number }[] = [];
  const nPoints = 50;
  const wlMin = 0.6;
  const wlMax = 5.0;

  for (let i = 0; i < nPoints; i++) {
    const wavelength = wlMin + (i / (nPoints - 1)) * (wlMax - wlMin);
    let flux = 1.0 + 0.002 * Math.sin(wavelength * 2);

    for (const dip of dips) {
      const dist = (wavelength - dip.pos) / dip.width;
      flux -= dip.depth * Math.exp(-0.5 * dist * dist);
    }

    flux += ((Math.sin(wavelength * 127.1 + i * 311.7) * 43758.5453) % 1) * 0.003;
    points.push({
      wavelength: Math.round(wavelength * 1000) / 1000,
      flux: Math.round(flux * 10000) / 10000,
    });
  }

  return points;
}

